    def __init__(self, parent):
        self.parent = parent

    def get_key_combination(
        self,
        key: qt.PyQt.QtCore.Qt.Key,
        modifiers: qt.PyQt.QtCore.Qt.KeyboardModifier,
    ) -> str:
        try:
            key_string = self.keys_dictionary[key]
        except:
            key_string = str(key)
        combined_list = [
            self.modifier_dictionary[x]
            for x in self.modifier_dictionary.keys()
            if (x & int(modifiers) != 0)
        ]
        combined_list.append(key_string)
        return "+".join(combined_list)
